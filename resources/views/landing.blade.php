    <h2 class="mt-2">GT3 RS 991</h2>
    <img src="/resources/img/Porsche.jpg" width="390" height="190" />
    <p>
        El Porsche 911 GT3 RS 991 es una variante de alto rendimiento del Porsche 911 de la generación 991, fabricado
        entre 2013 y 2019. Este automóvil cuenta con un motor de 4.0 litros naturalmente aspirado, tracción trasera y
        una gran cantidad de mejoras aerodinámicas. Su construcción ligera y su interior enfocado en las carreras lo
        hacen ideal para su uso en circuitos, ofreciendo una experiencia de conducción emocionante y precisa.</br>
    </p>
